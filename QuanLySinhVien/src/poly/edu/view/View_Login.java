    private boolean checkNull() {
        if(txtName.getText().isBlank() || txtPass.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Hãy điền đẩy đủ thông tin để đăng nhập");
            return false;
        }
        
        return true;
    }
