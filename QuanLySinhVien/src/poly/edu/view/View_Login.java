    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if(checkNull()){
            String username = txtName.getText();
            String pass = new String(txtPass.getPassword());
            User user = userService.getUserByUsernameAndPass(username, pass);
            if(user == null){
                JOptionPane.showMessageDialog(this, "Login Failed");
            }else{
                String role = user.getRole();
                if(role.equalsIgnoreCase("PDT")){
                    new View_QuanLySinhVien(user).setVisible(true);
                    dispose();
                }else if(role.equalsIgnoreCase("teacher")){
                    new View_QuanLyDiemSinhVien(user).setVisible(true);
                    dispose();
                }
                JOptionPane.showMessageDialog(this, "Login Succesfully!");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

