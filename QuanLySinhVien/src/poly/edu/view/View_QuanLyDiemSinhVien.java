    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String ma = txtFind.getText();
        if (ma.isBlank()) {
            thongbao("Vui lòng nhập mã sinh viên");
            txtFind.requestFocus();
            return;
        }

        listNow = gradeService.getListByStudentId(ma);
        fillToTable(listNow);
        if (listNow.isEmpty()) {
            thongbao("Không tìm thấy bản ghi nào phù hợp.");
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (id < 0) {
            thongbao("Vui lòng chọn một bản ghi để xóa");
            return;
        }

        if (xacnhan("Xóa thông tin điểm của sinh viên này?") == JOptionPane.YES_OPTION) {
            String tb = gradeService.deleteEntityById(listNow.get(id).getMaSV());
            rdoGetAll.setSelected(true);
            listNow = gradeService.getAllEntity();
            fillToTable(listNow);
            thongbao(tb);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

