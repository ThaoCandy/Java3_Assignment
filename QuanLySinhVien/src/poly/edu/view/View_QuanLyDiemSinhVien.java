    private void fillToTable(ArrayList<Grade> listNow) {
        if (this.listNow.isEmpty()) {
            return;
        }
        clearForm();
        tbModel.setRowCount(0);
        for (Grade e : listNow) {
            tbModel.addRow(new String[]{e.getMaSV(), studentService.getEntityById(e.getMaSV()).getHoTen(), e.getTiengAnh() + "", e.getTinHoc() + "", e.getGDTC() + "", e.getDiemTB() + ""});
        }
        id = 0;
        showDetail();

    }

    private void clearForm() {
        id = -1;
        updateState = false;

        txtFind.setText("");
        txtMaSV.setText("");
        txtHoTen.setText("");
        txtTiengAnh.setText("");
        txtTinHoc.setText("");
        txtGDTC.setText("");

        lbDiemTB.setText("");
        lbThongbao.setText("");

        tbList.clearSelection();

    }

    private void showDetail() {
        if (id < 0) {
            return;
        }

        tbList.setRowSelectionInterval(id, id);
        updateState = false;

        Grade e = listNow.get(id);

        txtMaSV.setText(e.getMaSV());
        txtHoTen.setText(studentService.getEntityById(e.getMaSV()).getHoTen());
        txtTiengAnh.setText(e.getTiengAnh() + "");
        txtTinHoc.setText(e.getTinHoc() + "");
        txtGDTC.setText(e.getGDTC() + "");
        lbDiemTB.setText(e.getDiemTB() + "");

        if (e.getId() == null) {
            lbThongbao.setText("Chưa nhập điểm.");
        } else {
            lbThongbao.setText("");
        }

        txtMaSV.setEditable(false);
        txtTiengAnh.setEditable(false);
        txtTinHoc.setEditable(false);
        txtGDTC.setEditable(false);

    }

    private Grade readForm(Integer GradeId) {
        String ma = txtMaSV.getText();
        String ta = txtTiengAnh.getText();
        String th = txtTinHoc.getText();
        String gdtc = txtGDTC.getText();

        if (ma.isBlank() || ta.isBlank() || th.isBlank() || gdtc.isBlank()) {
            thongbao("Vui lòng nhập đầy đủ thông tin trước khi thêm");
            return null;
        }

        if (studentService.getEntityById(ma) == null) {
            thongbao("Không có sinh viên nào có mã: " + ma);
            txtMaSV.requestFocus();
            return null;
        }
        String st = ". Phải là số điểm >= 0 và <= 10";
        if (!isNumberScore(ta)) {
            thongbao("Điểm tiếng anh không hợp lệ" + st);
            txtTiengAnh.requestFocus();
            return null;
        }

        if (!isNumberScore(th)) {
            thongbao("Điểm tin học không hợp lệ" + st);
            txtTinHoc.requestFocus();
            return null;
        }

        if (!isNumberScore(gdtc)) {
            thongbao("Điểm giáo dục thể chất không hợp lệ" + st);
            txtGDTC.requestFocus();
            return null;
        }

        return new Grade(GradeId, ma, Double.valueOf(ta), Double.valueOf(th), Double.valueOf(gdtc));

    }

